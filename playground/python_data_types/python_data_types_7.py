#!/usr/bin/python3

#convert using .lower() built-in function
print("Hello World".lower())
print("HELLO WORLD".lower())
print("heLLo woRlD".lower())


#convert using .upper() built-in function
print("hellO worlD".upper())
print("HeLLo WoRlD".upper())
print("hello world".upper())


#convert using .split() built-in function
print(".split(), split files into list format".split())
print(["phillins", "is", "handsome", "young", "python", "programmer"])


#convert using .find() built-in function
print("you can use .find to search for character in a string".find("search"))
print("let's find the lost chronicle book".find("lost"))


#convert using .replace built-in function
print("phillins     loves   story   book".replace("story", "programming"))
print("phillins is a quack programmer".replace("quack", "great"))
